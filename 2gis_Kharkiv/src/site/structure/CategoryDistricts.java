public class CategoryDistricts {
    CategoryDistricts (){}
    String label;
    String items;
    String primary_attributes;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getItems() {
        return items;
    }

    public void setItems(String items) {
        this.items = items;
    }

    public String getPrimary_attributes() {
        return primary_attributes;
    }

    public void setPrimary_attributes(String primary_attributes) {
        this.primary_attributes = primary_attributes;
    }
}
